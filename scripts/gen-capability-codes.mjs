#!/usr/bin/env node
/**
 * Fetches capability codes from the local dev API and generates
 * src/app/shared/types/capability-codes.generated.ts
 *
 * Usage: npm run gen:caps
 * Requires: local API running at http://localhost:5000
 */
import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const API_URL = process.env.API_URL ?? 'http://localhost:5000';
const OUTPUT = resolve(__dirname, '../src/app/shared/types/capability-codes.generated.ts');

async function main() {
	const url = `${API_URL}/api/admin/capabilities/roles/catalog`;
	console.log(`Fetching capabilities from ${url}...`);

	const res = await fetch(url);
	if (!res.ok) throw new Error(`API responded ${res.status}: ${await res.text()}`);

	const items = await res.json();
	const codes = items
		.map((c) => c.codigo)
		.filter(Boolean)
		.sort();

	if (codes.length === 0) throw new Error('No capabilities returned from API');

	const union = codes.map((c) => `\t| '${c}'`).join('\n');
	const content = [
		'// Auto-generated by: npm run gen:caps',
		`// Source: GET ${url}`,
		'// Do not edit manually — regenerate with: npm run gen:caps',
		'',
		`export type CapabilityCode =`,
		`${union};`,
		'',
	].join('\n');

	writeFileSync(OUTPUT, content, 'utf-8');
	console.log(`Generated ${codes.length} capability codes → ${OUTPUT}`);
}

main().catch((err) => {
	console.error('gen:caps failed:', err.message);
	process.exit(1);
});
